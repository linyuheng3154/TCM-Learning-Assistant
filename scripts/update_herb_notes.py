def update_formula_notes():
    """更新方剂数据中的炮制信息"""
    data_path = os.path.join(os.path.dirname(__file__), '..', 'data', 'formulas.json')
    
    with open(data_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    updated_count = 0
    
    for formula in data['formulas']:
        formula_name = formula['name']
        
        if formula_name in CLASSIC_PREPARATION:
            prep_rules = CLASSIC_PREPARATION[formula_name]
            
            for comp in formula.get('composition', []):
                herb_name = comp['herb']
                
                if herb_name in prep_rules:
                    new_note = prep_rules[herb_name]
                    if new_note and (not comp.get('note') or comp['note'] == ''):
                        comp['note'] = new_note
                        updated_count += 1
                        print(f"更新: {formula_name} - {herb_name} ({new_note})")
    
    # 更新元数据
    data['_metadata']['updated_at'] = '2026-03-19'
    data['_metadata']['last_updated'] = '2026-03-19'
    
    # 保存
    with open(data_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print(f"\n总计更新: {updated_count} 条炮制信息")
    return updated_count

if __name__ == '__main__':
    update_formula_notes()
